local draw_id = {
    size_x = "@W_1#Application.M.hmi.sv.max_micro[0]",
    size_y = "@W_1#Application.M.hmi.sv.max_micro[1]",
    p_count = "@W_1#Application.M.hmi.pf.p_count",
    p_x = "@W_1#Application.M.hmi.view_axis_micro[0]",
    p_y = "@W_1#Application.M.hmi.view_axis_micro[1]",
    p_index = "@W_1#Application.M.hmi.pf_p_index",
    p_scale_micro = "@W_1#Application.M.hmi.pf_p_scale_micro"
}

local draw_spec = {
    index_edge_count = 4,
    rect_w = 520,
    rect_h = 300,
    s_scale = 1e-3, -- from micron to mm
    p_scale = 2e-1, -- get from plc
}

local draw_pf_in_progress = 0
function draw_pf()
    if draw_pf_in_progress == 1 then
        return false
    end
    draw_pf_in_progress = 1
    local part = "1_CST_0"
    --
    local size = {x = draw_spec.rect_w, y = draw_spec.rect_h}
    local size_x = we_bas_getint(draw_id.size_x)
    local size_y = we_bas_getint(draw_id.size_y)
    if size_x ~= nil and size_y ~= nil and size_x > 10000 and size_y > 10000 then
        size.x = math.ceil(size_x * draw_spec.s_scale)
        size.y = math.ceil(size_y * draw_spec.s_scale)
    else
        draw_pf_in_progress = 0
        return false
    end
    draw_spec.p_scale = we_bas_getshort(draw_id.p_scale_micro)
    if draw_spec.p_scale ~= nil and draw_spec.p_scale > 0 then
        draw_spec.p_scale = 1 / draw_spec.p_scale -- from save position to mm
    else
        draw_pf_in_progress = 0
        return false
    end
    cus_curve.init(part, 1, 1)
    cus_curve.set_range(part, "x", 0, size.x)
    cus_curve.set_range(part, "y", 0, size.y)
    --
    draw_pf_axis(part, 1, size)
    draw_pf_path(part, 2, size)
    draw_pf_visual_path_dir(part, 3, size)
    draw_pf_visual_index(part, 4, size)
    draw_pf_visual_cursor(part, 5, size)
    --
    cus_curve.refresh(part)
    draw_pf_in_progress = 0
    return true
end

function draw_pf_axis(part, id, size)
    local axisStyle = {
        color = 0x000000,
        linetype = 1,
        width = 1,
        visible = 1,
    }
    cus_curve.set_line(part, id, 5, axisStyle)
    local axis = {0, 0, 0, size.y, size.x, size.y, size.x, 0, 0, 0}
    cus_curve.draw_line(part, id, axis)
end

function draw_pf_path(part, id, size)
    local pathStyle = {
        color = 0x7532a8,
        linetype = 0,
        width = 1,
        visible = 1,
    }
    cus_curve.set_line(part, id, 1000, pathStyle)
    local p_count = we_bas_getshort(draw_id.p_count)
    if p_count == nil then
        p_count = 0
    end
    local path = {}
    local valid = 0
    for i = 1, p_count do
        local x = we_bas_getshort(draw_id_px[i])
        local y = we_bas_getshort(draw_id_py[i])
        if x ~= nil and y ~= nil then
            table.insert(path, x * draw_spec.p_scale)
            table.insert(path, y * draw_spec.p_scale)
            valid = valid + 1
        end
    end
    local sx = size.x / draw_spec.rect_w
    local sy = size.y / draw_spec.rect_h
    if valid == 0 then
        path = draw_circle(0, 0, sx, sy, 7, math.pi / 4, 4)
    elseif valid == 1 then
        path = draw_circle(path[1], path[2], sx, sy, 7, math.pi / 4, 4)
    end
    cus_curve.draw_line(part, id, path)
end

function draw_pf_visual_path_dir(part, id, size)
    local pathDirStyle = {
        color = 0xff6600,
        linetype = 0,
        width = 1,
        visible = 1,
    }
    cus_curve.set_line(part, id, 6, pathDirStyle)
    local p_count = we_bas_getshort(draw_id.p_count)
    if p_count == nil then
        p_count = 0
    end
    local sx = size.x / draw_spec.rect_w
    local sy = size.y / draw_spec.rect_h
    local path_dir = {}
    local valid = false
    if p_count >= 2 then
        local a = {x = 0, y = 0}
        local b = {x = 0, y = 0}
        a.x = we_bas_getshort(draw_id_px[p_count])
        a.y = we_bas_getshort(draw_id_py[p_count])
        b.x = we_bas_getshort(draw_id_px[1])
        b.y = we_bas_getshort(draw_id_py[1])
        if a.x ~= nil and a.y ~= nil and b.x ~= nil and b.y ~= nil then
            a.x = a.x * draw_spec.p_scale
            a.y = a.y * draw_spec.p_scale
            b.x = b.x * draw_spec.p_scale
            b.y = b.y * draw_spec.p_scale
            local dx = b.x - a.x
            local dy = b.y - a.y
            local d = math.sqrt(dx * dx + dy * dy)
            if d > 1 then
                local u = {
                    x = dx / d,
                    y = dy / d
                }
                local n = {x = u.y, y = -u.x}
                path_dir[1] = a.x
                path_dir[2] = a.y
                path_dir[3] = b.x - u.x * 9 * sx
                path_dir[4] = b.y - u.y * 9 * sy
                path_dir[5] = b.x - (u.x * 12 - n.x * 5) * sx
                path_dir[6] = b.y - (u.y * 12 - n.y * 5) * sy
                path_dir[7] = b.x
                path_dir[8] = b.y
                path_dir[9] = b.x - (u.x * 12 + n.x * 5) * sx
                path_dir[10] = b.y - (u.y * 12 + n.y * 5) * sy
                path_dir[11] = path_dir[3]
                path_dir[12] = path_dir[4]
            else
                path_dir[1] = a.x
                path_dir[2] = a.y
                path_dir[3] = b.x
                path_dir[4] = b.y
            end
            valid = true
        end
    end
    if valid == false then
        path_dir = draw_circle(0, 0, sx, sy, 7, math.pi / 4, 4)
    end
    cus_curve.draw_line(part, id, path_dir)
end

function draw_pf_visual_index(part, id, size)
    local indexStyle = {
        color = 0x0000ff,
        linetype = 0,
        width = 2,
        visible = 1,
    }
    cus_curve.set_line(part, id, draw_spec.index_edge_count + 1, indexStyle)
    local p_index = we_bas_getshort(draw_id.p_index)
    local p_count = we_bas_getshort(draw_id.p_count)
    if p_index == nil or p_count == nil then
        return
    end
    local scale = draw_spec.p_scale
    local x = we_bas_getshort(draw_id_px[p_index + 1])
    local y = we_bas_getshort(draw_id_py[p_index + 1])
    if p_index >= p_count then
        x = we_bas_getint(draw_id.p_x)
        y = we_bas_getint(draw_id.p_y)
        scale = draw_spec.s_scale
    end
    if x == nil or y == nil then
        return
    end
    x = x * scale
    y = y * scale
    local sx = size.x / draw_spec.rect_w
    local sy = size.y / draw_spec.rect_h
    cus_curve.draw_line(part, id, draw_circle(x, y, sx, sy, 6, 0, draw_spec.index_edge_count))
end

function draw_pf_visual_cursor(part, id, size)
    local cursorStyle = {
        color = 0xff0000,
        linetype = 2,
        width = 1,
        visible = 1,
    }
    cus_curve.set_line(part, id, 5, cursorStyle)
    local x = we_bas_getint(draw_id.p_x)
    local y = we_bas_getint(draw_id.p_y)
    if x == nil or y == nil then
        return
    end
    x = x * draw_spec.s_scale
    y = y * draw_spec.s_scale
    local xo = size.x * 2
    local yo = size.y * 2
    local cursor = {}
    cursor[1] = x
    cursor[2] = y + yo
    cursor[3] = x
    cursor[4] = y - yo
    cursor[5] = x - xo
    cursor[6] = y - yo
    cursor[7] = x - xo
    cursor[8] = y
    cursor[9] = x + xo
    cursor[10] = y
    cus_curve.draw_line(part, id, cursor)
end

function draw_circle(x, y, sx, sy, r, rad, edge_count)
    vts = {}
    for i = 0, edge_count do
        local irad = rad + (math.pi * 2 * i) / edge_count
        vts[i * 2 + 1] = x + math.cos(irad) * r * sx
        vts[i * 2 + 2] = y + math.sin(irad) * r * sy
    end
    return vts
end